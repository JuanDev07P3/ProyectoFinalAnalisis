    private void btnGuardarCambiosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarCambiosActionPerformed
     try {
        // 1. Recoger los datos modificados del formulario
        int id = Integer.parseInt(txtIdCliente.getText());
        String nombre = txtNombreCliente.getText();
        String apellido = txtApellidoCliente.getText();
        String direccion = txtDireccionCliente.getText();
        String telefono = txtTelefonoCliente.getText();
        String email = txtEmailCliente.getText();

   
        clienteModificado = new Cliente(id, nombre, apellido, direccion, telefono, email);

   
        this.dispose();

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this,
                "Por favor, ingrese un valor num√©rico v√°lido para el ID.",
                "Error de Entrada",
                JOptionPane.ERROR_MESSAGE);
        clienteModificado = null;
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this,
                "Ocurri√≥ un error al guardar los cambios del cliente: " + e.getMessage(),
                "Error",
                JOptionPane.ERROR_MESSAGE);
        clienteModificado = null;
    }
    }//GEN-LAST:event_btnGuardarCambiosActionPerformed

